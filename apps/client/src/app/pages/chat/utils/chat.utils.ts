const FMT_BOLD = /\*(.*?)\*/g;
const FMT_ITALIC = /_(.*?)_/g;
const FMT_STRIKE = /~(.*?)~/g;
const FMT_CODE = /`(.*?)`/g;

/**
 * Formats message content with WhatsApp-style markup and sanitizes output
 * to prevent XSS attacks using Angular's DomSanitizer.
 */
export function formatMessageContent(content: string, sanitizer: DomSanitizer): SafeHtml {
  let html = content
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;');
  html = html.replace(FMT_BOLD, '<strong>$1</strong>');
  html = html.replace(FMT_ITALIC, '<em>$1</em>');
  html = html.replace(FMT_STRIKE, '<del>$1</del>');
  html = html.replace(FMT_CODE, '<code>$1</code>');
  html = html.replace(/\n/g, '<br>');
  // Sanitize to strip any potentially dangerous content
  const sanitized = sanitizer.sanitize(SecurityContext.HTML, html) || '';
  return sanitizer.bypassSecurityTrustHtml(sanitized);
}

export function formatFileSize(bytes: number): string {
  if (bytes < 1024) return bytes + ' B';
  if (bytes < 1024 * 1024) return (bytes / 1024).toFixed(1) + ' KB';
  return (bytes / (1024 * 1024)).toFixed(1) + ' MB';
}
